  /* ─── Core i18n Engine ─── */
  let currentLang = localStorage.getItem('eei_lang') || 'en';

  function t(key) {
    return (T[currentLang] && T[currentLang][key]) ||
           (T['en'] && T['en'][key]) ||
           key;
  }

  function applyTranslations() {
    /* textContent */
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = t(key);
    });
    /* innerHTML (for bold / spans inside) */
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      el.innerHTML = t(key);
    });
    /* placeholder */
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph')));
    });
    /* aria-label */
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
    });
    /* html lang attribute */
    document.documentElement.setAttribute('lang', currentLang === 'pt' ? 'pt' : 'en');
  }

  function switchLanguage() {
    currentLang = currentLang === 'en' ? 'pt' : 'en';
    localStorage.setItem('eei_lang', currentLang);
    applyTranslations();
  }

  /* ─── Init ─── */
  document.addEventListener('DOMContentLoaded', function () {
    applyTranslations();

    document.querySelectorAll('.lang-toggle').forEach(btn => {
      btn.addEventListener('click', switchLanguage);
    });
  });

